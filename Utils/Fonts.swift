import Foundation
class Fonts: NSObject {
    
    class func avenirLight(size: Float) -> UIFont {
        return UIFont(name: "Avenir-Light", size: CGFloat(size))!
    }
    
    class func avenirNextRegular(size: Float) -> UIFont {
        return UIFont(name: "AvenirNext-Regular", size: CGFloat(size))!
    }
    
    class func AvenirNextDemiBold(size: Float) -> UIFont {
        return UIFont(name: "AvenirNext-DemiBold", size: CGFloat(size))!
    }
    
    class func AvenirMedium(size: Float) -> UIFont {
        return UIFont(name: "Avenir-Medium", size: CGFloat(size))!
    }
    
    class func AvenirBook(size: Float) -> UIFont {
        return UIFont(name: "Avenir-Book", size: CGFloat(size))!
    }
    
    class func ArialMT(size: Float) -> UIFont {
        return UIFont(name: "ArialMT", size: CGFloat(size))!
    }
    
    class func AvenirMediumBold(size:Float) -> UIFont {
        return UIFont(name: "Avenir-Bold", size: CGFloat(size))!
    }
    
    class func AvenirNextMediumBold(size:Float) -> UIFont {
        return UIFont(name: "AvenirNext-Bold", size: CGFloat(size))!
    }
    
    
    
}
